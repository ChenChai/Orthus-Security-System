package com.example.orthusapp;

public class ListAlerts {

    private String alertTime;
    private String armed;

    public ListAlerts(String mTime, String mArmed){
        mTime = alertTime;
        mArmed = armed;
    }

    public String getAlertTime() {
        return alertTime;
    }
    public String getArmed() {
        return armed;
    }
}
